package com.tg.entity;

public class SceneryCluster {
	
	private String viewNo;
	
	private int nature;
	private int carve;
	private int archetecture;
	private int music;
	private int historyCulture;
	private int entertainment;
	private int catering;
	
	/*Getters and Setters*/
	public String getViewNo() {
		return viewNo;
	}
	public void setViewNo(String viewNo) {
		this.viewNo = viewNo;
	}
	public int getNature() {
		return nature;
	}
	public void setNature(int nature) {
		this.nature = nature;
	}
	public int getCarve() {
		return carve;
	}
	public void setCarve(int carve) {
		this.carve = carve;
	}
	public int getArchetecture() {
		return archetecture;
	}
	public void setArchetecture(int archetecture) {
		this.archetecture = archetecture;
	}
	public int getMusic() {
		return music;
	}
	public void setMusic(int music) {
		this.music = music;
	}
	public int getHistoryCulture() {
		return historyCulture;
	}
	public void setHistoryCulture(int historyCulture) {
		this.historyCulture = historyCulture;
	}
	public int getEntertainment() {
		return entertainment;
	}
	public void setEntertainment(int entertainment) {
		this.entertainment = entertainment;
	}
	public int getCatering() {
		return catering;
	}
	public void setCatering(int catering) {
		this.catering = catering;
	}
}
